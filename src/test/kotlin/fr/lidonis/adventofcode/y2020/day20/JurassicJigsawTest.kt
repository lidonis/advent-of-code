internal class JurassicJigsawTest {

    private val jurassicJigsaw = JurassicJigsaw(
        INPUT
    )

    @Test
    fun `corner tiles`() {
        assertThat(jurassicJigsaw.getCornerTiles().map { it.id }.toSet()).isEqualTo(
            setOf(
                1951,
                3079,
                2971,
                1171
            )
        )
    }

    @Test
    fun `water roughness`() {
       assertThat(jurassicJigsaw.waterRoughness()).isEqualTo(273)
    }
