internal class Day20Test {

    @Test
    fun `donut example 1`() {
        val maze = SpaceWarpingMaze(
            input = "         A           \n" +
                    "         A           \n" +
                    "  #######.#########  \n" +
                    "  #######.........#  \n" +
                    "  #######.#######.#  \n" +
                    "  #######.#######.#  \n" +
                    "  #######.#######.#  \n" +
                    "  #####  B    ###.#  \n" +
                    "BC...##  C    ###.#  \n" +
                    "  ##.##       ###.#  \n" +
                    "  ##...DE  F  ###.#  \n" +
                    "  #####    G  ###.#  \n" +
                    "  #########.#####.#  \n" +
                    "DE..#######...###.#  \n" +
                    "  #.#########.###.#  \n" +
                    "FG..#########.....#  \n" +
                    "  ###########.#####  \n" +
                    "             Z       \n" +
                    "             Z       "
        )
        assertThat(maze.shortestPathDonut()).isEqualTo(23)
    }

